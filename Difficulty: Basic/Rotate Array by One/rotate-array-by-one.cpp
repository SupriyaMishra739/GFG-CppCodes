class Solution {
public:
    

    void rotate(vector<int> &v)
    {
        
        int size=v.size();
        
        int temp=v[size-1];
        
        for(int i=size-1;i>0;i--){
            v[i]=v[i-1];
        }
        v[0]=temp;
       
            }
        
    
};


